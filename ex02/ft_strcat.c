char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	u;

	i = 0;
	u = 0;
	while (dest[i])
		i++;
	while (src[u])
	{
		dest[i + u] = src[u];
		u++;
	}
	dest[i + u] = 0;
	return (dest);
}
